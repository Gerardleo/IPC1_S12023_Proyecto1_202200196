    private void AgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AgregarActionPerformed
        try{
            String corr = correo.getText();
            String ape = apellido.getText();
            String nom = nombre.getText();
            String cont = getContrasena();
            String contVal = getContrasenaValid();
            String dp = dpi.getText();
            String date =   fechaNacimiento.getText();
            String gen =  genero.getSelectedItem().toString();
            String nac =  nacionalidad.getText();
            String snom = sobreNombre.getText();
            String tel = telefono.getText();
            String role = rol.getSelectedItem().toString();
         if(corr.equals("") || nom.equals("") || contrasena.getPassword().equals("") ){
            System.out.println("Ingrese los datos correspondientes");
             JOptionPane.showConfirmDialog(this, "Ingrese los datos correspondientes" , "Confirmar acción", JOptionPane.DEFAULT_OPTION);

        }else{
            if(validarUsuarios()==true || !(cont.equals(contVal))){
                System.out.println("Datos ya utilizados o la confirmacion de la contraseña no es igual");
                JOptionPane.showConfirmDialog(this, "Datos ya utilizados o la confirmacion de la contraseña no es igual" , "Confirmar acción", JOptionPane.DEFAULT_OPTION);
                
            }else{
                 if(validaContrasena(cont) == true){
                     listaUsuarios.add(new Usuario(corr, nom, ape,cont,dp,date,gen,nac,snom, tel, role));
                     System.out.println("Usuario Registrado: "+" Correo: "+correo.getText()+ " Nombre: "+nombre.getText()+" Apellido: "+ apellido.getText()+" Contrasena: "+contrasena.getText()+" DPI: "+dpi.getText()+" Fecha Nacimiento: "+fechaNacimiento.getText()+" Genero: "+genero.getSelectedItem()+" Nacionalidad: "+nacionalidad.getText()+" Sobre nombre: "+sobreNombre.getText()+" Telefono: "+telefono.getText()+" Rol: "+rol.getSelectedItem());
                     System.out.println("Usuario registrado exitosamente");
                     JOptionPane.showConfirmDialog(this, "Usuario registrado exitosamente" , "Confirmar acción", JOptionPane.DEFAULT_OPTION);
                     reseteoCampos();   
                 }else{
                     System.out.println("La contrasena no cumple con los tres requisitos, necesita Mayusculas, minusculas y caracteres especiales");
                     JOptionPane.showConfirmDialog(this, "La contrasena no cumple con los tres requisitos, necesita Mayusculas, minusculas y caracteres especiales" , "Confirmar acción", JOptionPane.DEFAULT_OPTION);
                 }      
            }
        }
        }catch(Exception e){
            System.out.println("Error, intente nuevamente");
            JOptionPane.showConfirmDialog(this, "Error, intente nuevamente" , "Confirmar acción", JOptionPane.DEFAULT_OPTION);

        }
       
    }//GEN-LAST:event_AgregarActionPerformed

