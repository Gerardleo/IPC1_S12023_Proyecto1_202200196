public boolean validarUsuarios(){
    for(int i=0; i < listaUsuarios.size(); i++ ){
            Usuario temp = new Usuario();
            temp = listaUsuarios.get(i);
            boolean encontro = false;
            if(temp.getCorreo().equals(correo.getText()) || temp.getTelefono().equals(telefono.getText()) || temp.getdpi().equals(dpi.getText())){
                return !encontro;
        }else{
                return false;
                } 
        }
    return false;
}

